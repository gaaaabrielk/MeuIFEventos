package com.example.meuifeventos;

public class Aluno {
        private String nome;
        private int id;

        public Aluno() {}

        public Aluno(String nome, int id) {
            this.nome = nome;
            this.id = id;
        }

        public String getNome() {
            return nome;
        }

        public int getid() {
            return id;
        }
    }


